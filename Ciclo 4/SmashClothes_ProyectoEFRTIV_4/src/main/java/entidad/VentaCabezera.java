package entidad;

public class VentaCabezera {
	private String numBol, tipoBol, pagadoBol, fecha, codCli;
	private double precioTo;

	public double getPrecioTo() {
		return precioTo;
	}

	public void setPrecioTo(double precioTo) {
		this.precioTo = precioTo;
	}

	public String getCodCli() {
		return codCli;
	}

	public void setCodCli(String codCli) {
		this.codCli = codCli;
	}

	public String getFecha() {
		return fecha;
	}

	public void setFecha(String fecha) {
		this.fecha = fecha;
	}

	public String getNumBol() {
		return numBol;
	}

	public void setNumBol(String numBol) {
		this.numBol = numBol;
	}

	public String getTipoBol() {
		return tipoBol;
	}

	public void setTipoBol(String tipoBol) {
		this.tipoBol = tipoBol;
	}

	public String getPagadoBol() {
		return pagadoBol;
	}

	public void setPagadoBol(String pagadoBol) {
		this.pagadoBol = pagadoBol;
	}

	
}
