package entidad;



public class Proveedor {
	private String codProv, represe, empresa, ruc, pais, correo, tele, fotoRu, fotoExte;
	private int estado;
	
	public String getFotoRu() {
		return fotoRu;
	}
	public void setFotoRu(String fotoRu) {
		this.fotoRu = fotoRu;
	}
	public String getFotoExte() {
		return fotoExte;
	}
	public void setFotoExte(String fotoExte) {
		this.fotoExte = fotoExte;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	public String getCodProv() {
		return codProv;
	}
	public void setCodProv(String codProv) {
		this.codProv = codProv;
	}
	public String getReprese() {
		return represe;
	}
	public void setReprese(String represe) {
		this.represe = represe;
	}
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public String getRuc() {
		return ruc;
	}
	public void setRuc(String ruc) {
		this.ruc = ruc;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getTele() {
		return tele;
	}
	public void setTele(String tele) {
		this.tele = tele;
	}
	
	
}
